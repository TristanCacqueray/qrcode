unicodeToKanji :: Word32 -> Maybe (Char, Word16)
unicodeToKanji cw =
  case convertStrictly "UTF-32BE" "Shift_JIS" cb of
    Left kc
      | BL.length kc == 2 ->
          case fromIntegral (kc `BL.index` 0) `shiftL` 8 + fromIntegral (kc `BL.index` 1) of
            k
              | k >= 0x8140 && k <= 0x9ffc -> Just (cc, go (k - 0x8140))
              | k >= 0xe040 && k <= 0xebbf -> Just (cc, go (k - 0xc140))
              | otherwise -> Nothing
    _ -> Nothing
  where
    go :: Word16 -> Word16
    go k = (k `shiftR` 8) * 0xc0 + (k .&. 0xff)
    cc :: Char
    cc = chr (fromIntegral cw)
    cb :: BL.ByteString
    cb =
      BL.pack
        [ fromIntegral (cw `shiftR` 24)
        , fromIntegral (cw `shiftR` 16)
        , fromIntegral (cw `shiftR` 8)
        , fromIntegral cw
        ]

showB :: Word8 -> String
showB c
  -- c0-ctrl    quote        backslash    del + c1-ctrl-chars        shy
